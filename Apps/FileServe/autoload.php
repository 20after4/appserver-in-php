<?php

namespace MFS\AppServer\Apps\FileServe;

function autoload($class_name)
{
    static $files = null;

    if (null === $files) {
        $root = __DIR__.'/';

        $files = array(
            'MFS\AppServer\Apps\FileServe\FileServe'               => $root.'FileServe.class.php',
        );
    }

    if (isset($files[$class_name]))
        require $files[$class_name];
}

spl_autoload_register('MFS\AppServer\Apps\FileServe\autoload');
